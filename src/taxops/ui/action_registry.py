@dataclass(frozen=True)
class UIActionContract:
    button_label: str
    page: str
    handler: str
    service: str | None
    repository: str | None
    success_text: str
    failure_text: str
    audit_action: str | None
    test_marker: str
    enabled: bool


def _disabled(label: str, page: str) -> "UIActionContract":
    return UIActionContract(
        button_label=label,
        page=page,
        handler=PLACEHOLDER_HANDLER,
        service=None,
        repository=None,
        success_text="",
        failure_text="",
        audit_action=None,
        test_marker="test_disabled_actions_marked",
        enabled=False,
    )


